/*
 ! لیست همه محصولات را نمایش دهید. اگر محصولی فروخته نشده است (فرض کنیم جدول order_items نداریم و صرفا می‌خواهیم تمرین CASE یا منطق شرطی کنیم)، در یک ستون جدید بنویسید “ناموجود” و اگر موجودی دارد بنویسید “موجود”.
 */
SELECT name,
    stock,
    CASE
        WHEN stock > 0 THEN 'موجود'
        ELSE 'ناموجود'
    END AS status
FROM products;